#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n,m;
    int a[100][100],v1,v2,v3,v4;
    scanf("%d %d",&n,&m);
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
             scanf("%d",&a[i][j]);
        }
    }
    scanf("%d ",&v1);
    scanf("%d ",&v2);
    scanf("%d ",&v3);
    scanf("%d ",&v4);
    return 0;
}
